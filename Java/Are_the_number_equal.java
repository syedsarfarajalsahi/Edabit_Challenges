public class Are_the_number_equal {
    public static void is_same_number(int a, int b) {
        if (a == b)
            System.out.println(true);
        else
            System.out.println(false);
    }

    public static void main(String[] args) {
        int x = 4, y = 5;
        is_same_number(x, y);
    }
}
